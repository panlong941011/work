        foreach ($arr as $v) {
            $arrObjectID[] = $v->ObjectID;
        }

        if ($arrObjectID) {
            return SysDep::find()->select(['ID', 'sName'])->where("ID IN ('" . implode("','",
                    $arrObjectID) . "')")->asArray()->all();
        } else {
            return [];
        }
    }

    public function getRoles()
    {
        $arrObjectID = [];
        $arr = SysOrgOperator::find()->select(['ObjectID'])->where("sObjectName='" . $this->sObjectName . "' AND sOperator='" . $this->sOperator . "' AND sOrgName='System/SysRole'")->all();
        foreach ($arr as $v) {
            $arrObjectID[] = $v->ObjectID;
        }

        if ($arrObjectID) {
