class SysObjectOperator extends \myerm\backend\system\models\Model
{
    public static function findOne($condition)
    {
        return \Yii::$app->cache->getOrSet(
            md5(serialize($condition)), function () use ($condition) {
            return parent::findOne($condition); // TODO: Change the autogenerated stub
        }
        );
    }

    public function getUsers()
    {
        $arrObjectID = [];
        $arr = SysOrgOperator::find()->select(['ObjectID'])->where("sObjectName='" . $this->sObjectName . "' AND sOperator='" . $this->sOperator . "' AND sOrgName='System/SysUser'")->all();
        foreach ($arr as $v) {
            $arrObjectID[] = $v->ObjectID;
        }

        if ($arrObjectID) {
            return SysUser::find()->select(['ID', 'sName'])->where("ID IN ('" . implode("','",
                    $arrObjectID) . "')")->asArray()->all();
        } else {
            return [];
        }
    }

    public function getDeps()
    {
        $arrObjectID = [];
